    t: (key) => {
        const lang = window.I18N.getLang();
        return (window.I18N.translations[lang] && window.I18N.translations[lang][key]) || (window.I18N.translations['en'][key]) || key;
    },
    translatePage: () => {
        const lang = window.I18N.getLang();
        document.documentElement.dir = (lang === 'ar' ? 'rtl' : 'ltr');
        
        // Font & RTL Style Injection
        if (!document.getElementById('ar-fonts')) {
            const link = document.createElement('link');
            link.id = 'ar-fonts';
            link.rel = 'stylesheet';
            link.href = 'https://fonts.googleapis.com/css2?family=Noto+Sans+Arabic:wght@400;700;900&display=swap';
            document.head.appendChild(link);
            
            const style = document.createElement('style');
            style.id = 'ar-styles';
            style.innerHTML = `
                [dir="rtl"] { font-family: 'Noto Sans Arabic', sans-serif !important; }
                [dir="rtl"] h1, [dir="rtl"] h2 { font-family: 'Noto Sans Arabic', sans-serif !important; }
                /* Flip back buttons and icons */
                [dir="rtl"] .back-btn, [dir="rtl"] .back { transform: scaleX(-1); display: inline-block; }
                [dir="rtl"] .chevron { transform: scaleX(-1); display: inline-block; }
                [dir="rtl"] .nav-item { flex-direction: column !important; }
                [dir="rtl"] .item-left { flex-direction: row-reverse; }
                [dir="rtl"] .input-box { flex-direction: row-reverse; }
            `;
            document.head.appendChild(style);
        }

        document.querySelectorAll('[data-t]').forEach(el => {
            const key = el.getAttribute('data-t');
            const translation = window.I18N.t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.type === 'button' || el.type === 'submit') el.value = translation;
                else el.placeholder = translation;
            } else el.innerText = translation;
        });
        document.documentElement.lang = lang;
    }
